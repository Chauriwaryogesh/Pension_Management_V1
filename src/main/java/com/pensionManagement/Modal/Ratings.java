package com.pensionManagement.Modal;

public class Ratings {
	private int dashboard;
	private int bank_services;
	private int payment;
	private int policy;
	private int policy_admin;
	private int admin_module;

	public int getDashboard() {
		return dashboard;
	}

	public void setDashboard(int dashboard) {
		this.dashboard = dashboard;
	}

	public int getBank_services() {
		return bank_services;
	}

	public void setBank_services(int bank_services) {
		this.bank_services = bank_services;
	}

	public int getPayment() {
		return payment;
	}

	public void setPayment(int payment) {
		this.payment = payment;
	}

	public int getPolicy() {
		return policy;
	}

	public void setPolicy(int policy) {
		this.policy = policy;
	}

	public int getPolicy_admin() {
		return policy_admin;
	}

	public void setPolicy_admin(int policy_admin) {
		this.policy_admin = policy_admin;
	}

	public int getAdmin_module() {
		return admin_module;
	}

	public void setAdmin_module(int admin_module) {
		this.admin_module = admin_module;
	}

}
