package com.pensionManagement.Modal;

public class Queue {
	private String complaintsTeam;
	private String workflowTeam;
	private String adminTeam;
	private String teamMember;

	/**
	 * @return the complaintsTeam
	 */
	public String getComplaintsTeam() {
		return complaintsTeam;
	}

	/**
	 * @param complaintsTeam the complaintsTeam to set
	 */
	public void setComplaintsTeam(String complaintsTeam) {
		this.complaintsTeam = complaintsTeam;
	}

	/**
	 * @return the workflowTeam
	 */
	public String getWorkflowTeam() {
		return workflowTeam;
	}

	/**
	 * @param workflowTeam the workflowTeam to set
	 */
	public void setWorkflowTeam(String workflowTeam) {
		this.workflowTeam = workflowTeam;
	}

	/**
	 * @return the adminTeam
	 */
	public String getAdminTeam() {
		return adminTeam;
	}

	/**
	 * @param adminTeam the adminTeam to set
	 */
	public void setAdminTeam(String adminTeam) {
		this.adminTeam = adminTeam;
	}

	/**
	 * @return the teamMember
	 */
	public String getTeamMember() {
		return teamMember;
	}

	/**
	 * @param teamMember the teamMember to set
	 */
	public void setTeamMember(String teamMember) {
		this.teamMember = teamMember;
	}

}
